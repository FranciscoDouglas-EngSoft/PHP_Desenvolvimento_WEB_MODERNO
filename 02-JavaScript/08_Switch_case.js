//Estrutura switch case
let expression = 'a';
switch (expression) {
    case 'a':
        //Bloco de código
        console.log("A letra é 'a'");
        break;
    case 'b':
        //Bloco de código
        console.log("A letra é 'b'");
        break;
    default:
        //Bloco de código
        console.log("A letra não é 'a' nem 'b'");
}