//Strings e Metodos de String em JavaScript

let texto = "Olá, Mundo!";
let frase = 'JavaScript é divertido.';
let mensagem = `Hoje é um ótimo dia para aprender JavaScript!`;

console.log(texto);
console.log(frase);
console.log(mensagem);

// Métodos de String
console.log("Tamanho do texto:", texto.length);
console.log("Texto em maiúsculas:", texto.toUpperCase());
console.log("Texto em minúsculas:", texto.toLowerCase());
console.log("Substituição:", texto.replace("Mundo", "JavaScript"));
console.log("Divisão:", mensagem.split(" "));